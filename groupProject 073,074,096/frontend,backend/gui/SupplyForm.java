    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        try{
            Connection con = ConnectionProvider.getCon();
            Statement stmt = null;
            //Step 2 : Initialize Statement
	    stmt=con.createStatement();
	    //Step 3 : SQL Query
	    String query="SELECT * FROM SUPPLIES WHERE SUPPLIESID='"+this.jTextField13.getText()+"'";
	    
	    //Step 4 : Run Query In ResultSet
	    ResultSet rset = stmt.executeQuery(query);
            boolean flag =rset.next();
                if(!flag){
                    JOptionPane.showMessageDialog(null, "RECORD DOES NOT FOUND");
                    this.jTextField13.setText("");
                }
            while(flag)
	    {
	    	this.jTextField5.setText(rset.getString(1));
                this.jTextField8.setText(rset.getString(2));
                this.jTextField9.setText(rset.getString(3)); 
                flag = rset.next();
	    }
            String query1="SELECT * FROM DEALER WHERE DEALID='"+this.jTextField9.getText()+"'";
	    rset = stmt.executeQuery(query1);
            flag =rset.next();
                if(!flag){
                    JOptionPane.showMessageDialog(null, "DEALER RECORD DOES NOT FOUND");
                    this.jTextField13.setText("");
                }
            while(flag)
	    {
                this.jTextField10.setText(rset.getString(2));
                this.jTextField11.setText(rset.getString(3));
	    	this.jTextField12.setText(rset.getString(4));
                flag = rset.next();
	    }
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null, "Invalid ID");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

