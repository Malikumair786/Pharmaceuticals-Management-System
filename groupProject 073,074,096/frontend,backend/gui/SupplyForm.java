    public void setHistorySupplyPanelTrue() throws SQLException, ClassNotFoundException{
        this.jPanel3.setVisible(true);
        DefaultTableModel model = (DefaultTableModel)this.jTable1.getModel();
        //Object[][] row = new Object[20][6];
        try{
            Connection con = ConnectionProvider.getCon();
            Statement stmt = null;
            //Step 2 : Initialize Statement
	    stmt=con.createStatement();
	    //Step 3 : SQL Query
	    String query="SELECT * FROM SUPPLIES ";
	    
	    //Step 4 : Run Query In ResultSet
	    ResultSet rset = stmt.executeQuery(query);
            boolean flag =rset.next();
                if(!flag){
                    JOptionPane.showMessageDialog(null, "RECORDS DOES NOT FOUND");
                }
            ArrayList<supply> row = new ArrayList();
            supply obj ;
            while(flag )
	    {
                obj = new supply();
                obj.suppplyid = (String)rset.getString(2);
                obj.storeid = (String)rset.getString(1);
                obj.dealerid = (String)rset.getString(3);
                flag = rset.next();
                row.add(obj);
	    }
            String query1="SELECT * FROM DEALER ";
	    rset = stmt.executeQuery(query1);
            flag =rset.next();
                if(!flag){
                    JOptionPane.showMessageDialog(null, "DEALER RECORDS DOES NOT FOUND");
                }
                int i=0;
            while(flag )
	    {
                row.get(i).dealerid = (String)rset.getString(1);
                row.get(i).name = (String)rset.getString(2);
                row.get(i).address = (String)rset.getString(3);
                row.get(i).phoneno = (String)rset.getString(4);
                flag = rset.next();
                i++;
            }
            i = 0;
            while(i < row.size()){
                obj = (supply)row.get(i);
                model.addRow(new Object[]{obj.suppplyid, obj.storeid, 
                obj.dealerid, obj.phoneno, obj.address, obj.name });
                i++;
            }
        }
        catch(Exception e){
            System.out.println("Error occured");
        }
    }
