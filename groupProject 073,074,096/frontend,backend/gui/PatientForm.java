    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try{
            Connection con = ConnectionProvider.getCon();
            Statement stmt = null;
            //Step 2 : Initialize Statement
	    stmt=con.createStatement();
	    //Step 3 : SQL Query
	    String query="SELECT * FROM PATIENT WHERE PATID='"+this.search_pat_id.getText()+"'";
	    
	    //Step 4 : Run Query In ResultSet
	    ResultSet rset = stmt.executeQuery(query); 
            boolean flag =rset.next();
            System.out.println(flag);
                if(!flag){
                    JOptionPane.showMessageDialog(null, "RECORD DOES NOT FOUND");
                    this.search_pat_id.setText("");
                }
            while(flag)
	    {
	    	this.update_pat_id.setText(rset.getString(1));
                this.update_name.setText(rset.getString(2));
                this.update_address.setText(rset.getString(3));
                this.update_phone.setText(rset.getString(4));
                flag= rset.next();
	    }
            
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null, "Invalid ID");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

