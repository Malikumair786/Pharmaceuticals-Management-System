    public void setHistoryPatientPanelTrue(){
        this.jPanel3.setVisible(true);
        DefaultTableModel model = (DefaultTableModel)this.drugTable2.getModel();
        //Object[][] row = new Object[20][6];
        try{
            Connection con = ConnectionProvider.getCon();
            Statement stmt = null;
            //Step 2 : Initialize Statement
	    stmt=con.createStatement();
	    //Step 3 : SQL Query
	    String query="SELECT * FROM PATIENT ";
	    
	    //Step 4 : Run Query In ResultSet
	    ResultSet rset = stmt.executeQuery(query);
            boolean flag =rset.next();
                if(!flag){
                    JOptionPane.showMessageDialog(null, "NO RECORD FOUND");
                }
            ArrayList<patient> row = new ArrayList();
            patient obj ;
            while(flag )
	    {
                obj = new patient();
                obj.patid = (String)rset.getString(1);
                obj.name = (String)rset.getString(2);
                obj.address = (String)rset.getString(3);
                obj.phoneno = (String)rset.getString(4);
                flag = rset.next();
                row.add(obj);
	    }
            int i = 0;
            while(i < row.size()){
                obj = (patient)row.get(i);
                model.addRow(new Object[]{obj.patid, obj.name, obj.address, obj.phoneno });
                i++;
            }
        }
        catch(Exception e){
            System.out.println("Error occured");
        }
    }
