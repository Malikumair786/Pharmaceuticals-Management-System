    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try{
            Connection con = ConnectionProvider.getCon();
            Statement stmt = null;
            //Step 2 : Initialize Statement
	    stmt=con.createStatement();
	    //Step 3 : SQL Query
	    String query="SELECT * FROM TREATMENTS WHERE TREATID='"+this.treatment_id.getText()+"'";
	    
	    //Step 4 : Run Query In ResultSet
	    ResultSet rset = stmt.executeQuery(query);
            boolean flag =rset.next();
                if(!flag){
                    JOptionPane.showMessageDialog(null, "RECORD DOES NOT FOUND");
                    this.treatment_id.setText("");
                }
            while(flag)
	    {
	    	this.add_doc_id1.setText(rset.getString(1));
                this.add_hos_id1.setText(rset.getString(2));
                this.add_treat_date1.setText(rset.getString(4));
                this.add_pat_id1.setText(rset.getString(5));
                flag = rset.next();
	    }
            
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null, "Invalid ID");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

