    public void setHistoryPanelTrue(){
        this.jPanel3.setVisible(true);
        DefaultTableModel model = (DefaultTableModel)this.drugTable2.getModel();
        //Object[][] row = new Object[20][6];
        try{
            Connection con = ConnectionProvider.getCon();
            Statement stmt = null;
            //Step 2 : Initialize Statement
	    stmt=con.createStatement();
	    //Step 3 : SQL Query
	    String query="SELECT * FROM TREATMENTS ";
	    
	    //Step 4 : Run Query In ResultSet
	    ResultSet rset = stmt.executeQuery(query);
            boolean flag =rset.next();
                if(!flag){
                    JOptionPane.showMessageDialog(null, "NO RECORD FOUND");
                }
            ArrayList<treatment> row = new ArrayList();
            treatment obj ;
            while(flag )
	    {
                obj = new treatment();
                obj.docid= (String)rset.getString(1);
                obj.hosid = (String)rset.getString(2);
                obj.treatid = (String)rset.getString(3);
                obj.treatdate = (String)rset.getString(4);
                obj.patid = (String)rset.getString(5);
                flag = rset.next();
                row.add(obj);
	    }
            int i = 0;
            while(i < row.size()){
                obj = (treatment)row.get(i);
                model.addRow(new Object[]{obj.treatid, obj.hosid, obj.patid, obj.docid, null, obj.treatdate });
                i++;
            }
        }
        catch(Exception e){
            System.out.println("Error occured");
        }
    }
